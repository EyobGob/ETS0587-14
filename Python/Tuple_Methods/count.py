# Using String
fruits = ('apple', 'banana', 'apple', 'orange', 'apple', 'grape')

number_of_apples = fruits.count('apple')
print(f"The fruit 'apple' appears {number_of_apples} times in the tuple.") # Output: The fruit 'apple' appears 3 times in the tuple.

# Using Integer
numbers = (10, 20, 10, 30, 10, 40, 10)

count_of_ten = numbers.count(10)
print(f"The number 10 appears {count_of_ten} times in the tuple.") # Output: The number 10 appears 4 times in the tuple.