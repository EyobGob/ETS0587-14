my_dictionary = {'name': 'Alice', 'age': 30, 'city': 'New York'}

print(f"Original dictionary: {my_dictionary}")  # Output: Original dictionary: {'name': 'Alice', 'age': 30, 'city': 'New York'}
print(f"Length of original dictionary: {len(my_dictionary)}") # Output: Length of original dictionary: 3

my_dictionary.clear()
print(f"Dictionary after clear: {my_dictionary}")   # Output: Dictionary after clear: {}
print(f"Length after clear: {len(my_dictionary)}")  # Output: Length after clear: 0