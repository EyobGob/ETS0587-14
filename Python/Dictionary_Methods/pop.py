ages = {"Nati": 30, "Abel": 25, "Biruk": 35}
print("Original dictionary:", ages) # Output: Original dictionary: {'Nati': 30, 'Abel': 25, 'Biruk': 35} 

Abel_age = ages.pop("Abel")
print("Age of Abel:", Abel_age) # Output: Age of Abel: 25
print("Dictionary after pop:", ages) # Output: Dictionary after pop: {'Nati': 30, 'Biruk': 35}



